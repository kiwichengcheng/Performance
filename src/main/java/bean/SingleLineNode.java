package bean;

import java.util.Date;

/**
 *
 * @author U6013046
 */
public class SingleLineNode {
    
    private Date date;
    private float time;
    private int fields;
    private int ilcount;

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public float getTime() {
        return time;
    }

    public void setTime(float time) {
        this.time = time;
    }

    public int getFields() {
        return fields;
    }

    public void setFields(int fields) {
        this.fields = fields;
    }

    public int getIlcount() {
        return ilcount;
    }

    public void setIlcount(int ilcount) {
        this.ilcount = ilcount;
    }
    
    
}
